def same_first_and_last_letter(word):
    return word[0] == word[-1]
print(same_first_and_last_letter("leo"))    
print(same_first_and_last_letter("lel"))


def three_number_sum(string1, string2, string3):
    return len(string1) + len(string2) + len(string3)
    
print(three_number_sum("leo", "cynthia", "chinago"))

